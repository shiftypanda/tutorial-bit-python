# Write a Python function, evalQuadratic(a, b, c, x),
# that returns the value of solved quadratic

def evalQuadtratic(a, b, c, x):
    """
    :param a: numerical value for coefficient of a quadratic
    :param b: ''
    :param c: ''
    :param x: numerical value at which to evaluate the quadratic
    :return:
    """
    return (a*(x**2)+(b*x) + c)